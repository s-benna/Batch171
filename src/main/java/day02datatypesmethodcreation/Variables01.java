package day02datatypesmethodcreation;

public class Variables01 {
    public static void main(String[] args) {

        //Ornek 1: Sehir ismi icin bir variable olusturun ve once "Ankara" sonrada "Izmir" degeri atayip ekrana yazdirin.
        String cityName= "Ankara";
        System.out.println(cityName);

        cityName ="Izmir";
        System.out.println(cityName);

        //Ornek 2: Sehir plaka kodu icin bir variable olusturun ve once "06 sonrada "35" degri atayip ekrana yazdirin.

        byte plakaKodu = 06;
        System.out.println(plakaKodu);

        plakaKodu = 35;
        System.out.println(plakaKodu);

        //Ornek 3: Basarili mi sorusu icin bir variable olusturn. Once basarisiz sonra da dogru degeri atayip ekrana yazdirin.

        boolean basarliMi= false;
        System.out.println(basarliMi);
        basarliMi = true;
        System.out.println(basarliMi);




























    }//main
}//class
